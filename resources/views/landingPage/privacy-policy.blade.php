            <div class="section">
                <h2>Introduction</h2>
                <p>We, at the City Agriculture Office of San Pedro, Laguna, protect the privacy of your personal and sensitive personal information ("personal data") and commit to comply with the Republic Act No. 10173 or the Data Privacy Act ("DPA"), its Implementing Rules and Regulations ("IRR"), and other relevant issuances of the National Privacy Commission. Our goal is to protect your personal data on the channels you interact with us – through the AgriSys platform (agrisys.site), our office premises, physical and digital forms submitted, and other service delivery touchpoints.</p>
                <p>This Privacy Notice outlines our approach on how we protect your privacy and explains in general terms the purpose and legal basis for the processing of personal data that the City Agriculture Office collects from you through AgriSys, the measures in place to protect your data privacy, and the rights that you may exercise in relation to such information. Please note that this document does not contain an exhaustive list of all processing systems as well as the purpose and legal basis for processing.</p>
            </div>

            <div class="section">
                <h2>Why We Collect and Process Personal Data</h2>
                <p>In general, the City Agriculture Office collects, uses and discloses personal data for the following purposes:</p>
                <ol>
                    <li>To register and maintain an updated database of farmers, fisherfolk, livestock raisers, and poultry raisers in San Pedro City for the Registry System for Basic Sectors in Agriculture (RSBSA);</li>
                    <li>To process and manage requests for agricultural supplies including seedlings, fertilizers, fingerlings, fruit-bearing trees, forest trees, and organic fertilizers;</li>
                    <li>To monitor agricultural supply inventory levels and generate alerts for timely restocking and equitable distribution;</li>
                    <li>To process applications for training programs, seminars, agricultural fairs, and capacity-building activities;</li>
                    <li>To generate prescriptive analytics and decision support insights using large language model technology to identify trends in crop demand, supply distribution, and beneficiary needs;</li>
                    <li>To conduct comprehensive stakeholder analysis and needs prioritization for agricultural programs;</li>
                    <li>To respond to court orders, instructions and requests from authorities including regulatory, governmental, and law enforcement;</li>
                    <li>To discharge our mandate pursuant to Philippine laws, Local Government Code provisions, and Department of Agriculture programs;</li>
                    <li>To respond to, process and handle your queries, requests, feedback, suggestions and complaints;</li>
                    <li>To conduct studies and research for the purpose of reviewing, developing and improving our provision of agricultural services in accordance with our mandate;</li>
                    <li>To prepare reports required by the Local Government Unit, Department of Agriculture, and other government agencies; and</li>
                    <li>To prevent, detect, investigate fraud and manage the safety and security of our premises and digital systems (including but not limited to maintaining system activity logs and access controls).</li>
                </ol>
            </div>

            <div class="section">
                <h2>What We May Collect From You</h2>
                <p>The types of personal data we collect and share depend on the means by which it was collected and your interactions with AgriSys. This may include, among others:</p>

                <div class="subsection">
                    <h4>Personal Information</h4>
                    <ol>
                        <li>Basic personal information like your name, date of birth, sex/gender, marital status, and citizenship, including supporting documents such as government-issued ID details (front and back images);</li>
                        <li>Your contact details like your complete address, barangay, mobile number, and email address (if available);</li>
                        <li>Agricultural profile information including your sector (farmer, fisherfolk, livestock raiser, poultry raiser), primary commodity or crop, farm location and size (land area in hectares), type of farming or fishing activity, livestock and poultry inventory, and fishing vessel information for registered fisherfolk;</li>
                        <li>Supporting documents that verify your status as a farmer, fisherfolk, livestock raiser, or poultry raiser (e.g., certificates, permits, land titles, barangay certifications);</li>
                        <li>Transaction records including supply requests, quantities and types of agricultural inputs requested, request dates and approval status, distribution records and acknowledgment receipts;</li>
                        <li>Training and seminar participation records, agricultural fair and community event attendance;</li>
                        <li>Specimen signatures on acknowledgment receipts and official forms; and</li>
                        <li>Images via CCTV and other similar recording devices which may be observed when visiting our office and/or using our facilities.</li>
                    </ol>
                </div>

                <div class="subsection">
                    <h4>Technical Information</h4>
                    <p>We collect non-personal identification information about users whenever they interact with AgriSys for security and analytics purposes. Non-personal identification information may include IP address, browser type and version, device type (desktop, mobile, tablet), access timestamps, and other technical information used as the means of connection, such as operating system. This type of information cannot be used to identify individual visitors unless necessary for security investigations.</p>
                </div>
            </div>

            <div class="section">
                <h2>How We May Share Your Data</h2>
                <p>The City Agriculture Office does not and will not share your personal data with third parties unless necessary for the accomplishment of the above-mentioned purposes and unless you give your consent thereto. Such third parties may include other departments of the San Pedro City Government, agents, outsourced service providers, and other third parties.</p>

                <div class="subsection">
                    <h4>Government Agencies</h4>
                    <p>Our third parties include other government agencies/bodies from the executive, legislative and judicial branches of government, including the Department of Agriculture, Philippine Statistics Authority, and other national agencies for statistical reporting, policy development, and program monitoring.</p>
                </div>

                <div class="subsection">
                    <h4>Technology Service Providers</h4>
                    <p>We engage outsourced service providers to achieve utmost support in delivering services to you. AgriSys utilizes the following technology service providers:</p>
                    <ol>
                        <li><span class="emphasis">Anthropic (Claude Sonnet 4.5)</span> – for large language model analytics and prescriptive decision support insights;</li>
                        <li><span class="emphasis">Hostinger</span> – for web hosting and server infrastructure;</li>
                        <li><span class="emphasis">Cloudflare</span> – for content delivery, security, and DDoS protection;</li>
                        <li><span class="emphasis">Philippine Short Message Service Gateway</span> – for bulk short message service notifications and alerts; and</li>
                        <li><span class="emphasis">Simple Mail Transfer Protocol</span> – for reliable email transmission and communication.</li>
                    </ol>
                    <p>Any personal data shared with third parties shall be covered by the appropriate agreement to ensure that all personal data is adequately safeguarded. This City Agriculture Office does not and will not sell personal data to any third party.</p>
                    <p><span class="emphasis">Note:</span> While AgriSys currently does not integrate with other government platforms, future updates may enable data sharing with relevant agencies to improve service coordination. Any such integration will be implemented in accordance with data protection laws, and you will be notified of significant changes to our data sharing practices.</p>
                </div>
            </div>

            <div class="section">
                <h2>How We Protect Your Data</h2>
                <p>The City Agriculture Office implements technical, organizational and physical security measures to protect your personal data against loss, misuse, modification, unauthorized or accidental access or disclosure, alteration or destruction. We put the following safeguards in place:</p>
                <ol>
                    <li>Role-based access control with encrypted login credentials ensuring only authorized City Agriculture Office personnel can access AgriSys;</li>
                    <li>Use of a secured server behind a firewall with intrusion detection systems;</li>
                    <li>Deployment of encryption on computers and devices, with all passwords encrypted using industry-standard hashing algorithms and secure HTTPS protocols for data transmission;</li>
                    <li>Use of antivirus and malware protection with updated security patches;</li>
                    <li>Regular automated backups to ensure data can be restored in case of system failure or data loss;</li>
                    <li>System activity logs tracking all data access and modifications for audit and accountability purposes; and</li>
                    <li>Establishment of physical security controls including restricted access to office premises and secure storage of paper records in locked cabinets.</li>
                </ol>
                <p style="margin-top: 15px;">Moreover, we restrict access to your personal data only to qualified and authorized City Agriculture Office personnel (City Head Agriculturist, City Agriculturists, and designated staff) who hold your personal data with strict confidentiality.</p>
                <p>We likewise train our employees to properly handle your data and comply with data privacy principles and security protocols.</p>
                <p>Despite these measures, no system can guarantee absolute security. While we strive to protect your data using industry best practices, we encourage you to safeguard your own account credentials and report any suspected security incidents to our office immediately.</p>
            </div>

            <div class="section">
                <h2>How We Store and Dispose Your Data</h2>
                <p>The City Agriculture Office retains and stores your personal data only according to operational needs and in compliance with its legal mandates. Our data retention and disposal policy is in accordance with Republic Act No. 9470 or the National Archives of the Philippines Act and applicable laws. Generally, this City Agriculture Office shall only retain your data for five (5) years after the processing relevant to the purpose has been terminated or after your last transaction with our office.</p>

                <div class="subsection">
                    <h4>Extended Retention</h4>
                    <p>However, this City Agriculture Office may retain and store your personal data beyond five years when:</p>
                    <ol>
                        <li>Required by law or regulation;</li>
                        <li>Necessary to establish, exercise or defend legal claims;</li>
                        <li>Needed for ongoing agricultural programs or longitudinal studies;</li>
                        <li>You remain an active beneficiary receiving services from the City Agriculture Office; or</li>
                        <li>When provided by law for legitimate purposes.</li>
                    </ol>
                </div>

                <div class="subsection">
                    <h4>Data Disposal</h4>
                    <p>When your personal data is no longer necessary to achieve any of the above-mentioned purposes, this City Agriculture Office destroys, deletes or disposes of the same following the security standards. Digital records are securely deleted from AgriSys databases and backup systems using data erasure methods that prevent recovery. Paper documents and physical records are shredded or incinerated following secure disposal protocols. Data used for statistical or research purposes may be anonymized or de-identified so that it can no longer identify you personally.</p>
                </div>
            </div>

            <div class="section">
                <h2>Your Data Privacy Rights</h2>
                <p>Under the DPA, you have the following rights:</p>

                <div class="subsection">
                    <h4>1. Right to be Informed</h4>
                    <p>You have the right to be informed about how your personal data is being collected, used, and shared. This Privacy Notice serves that purpose.</p>
                </div>

                <div class="subsection">
                    <h4>2. Right to Object</h4>
                    <p>You have the right to object to the processing of your personal data, including processing for direct marketing purposes, subject to limitations when processing is required by law or necessary for the performance of official functions.</p>
                </div>

                <div class="subsection">
                    <h4>3. Right to Access</h4>
                    <p>You have the right to reasonable access to your personal data held by AgriSys, including basic profile information, supply request history, training records, and documents you submitted.</p>
                </div>

                <div class="subsection">
                    <h4>4. Right to Rectify or Correct Erroneous Data</h4>
                    <p>If you believe that your personal data is inaccurate, incomplete, or outdated, you have the right to request correction or update with supporting documentation.</p>
                </div>

                <div class="subsection">
                    <h4>5. Right to Erase or Block</h4>
                    <p>You have the right to request deletion or blocking of your personal data under certain circumstances, subject to limitations when we are required to retain your data for legal compliance, ongoing programs, or to fulfill our mandate as a government office.</p>
                </div>

                <div class="subsection">
                    <h4>6. Right to Secure Data Portability</h4>
                    <p>You have the right to obtain a copy of your personal data in a structured, commonly used, and machine-readable format (e.g., PDF, Excel).</p>
                </div>

                <div class="subsection">
                    <h4>7. Right to be Indemnified for Damages</h4>
                    <p>You have the right to be indemnified for any damages sustained due to inaccurate, incomplete, outdated, false, unlawfully obtained, or unauthorized use of your personal data.</p>
                </div>

                <div class="subsection">
                    <h4>8. Right to File a Complaint</h4>
                    <p>If you believe your data privacy rights have been violated, you have the right to file a complaint with the City Agriculture Office Data Protection Officer or the National Privacy Commission.</p>
                </div>

                <p style="margin-top: 20px;">For more details on your above-enumerated rights, you may refer to Section 16 of the DPA.</p>

                <div class="subsection">
                    <h4>How to Exercise Your Rights</h4>
                    <p>To exercise any of these rights, submit a written request to our Data Protection Officer (contact details below). We will respond to your request within a reasonable timeframe, typically within 15 to 30 days, and may require identity verification to protect your data from unauthorized access.</p>
                </div>
            </div>

            <div class="section">
                <h2>Children's Privacy</h2>
                <p>AgriSys is designed for use by adult beneficiaries (18 years and older) who are farmers, fisherfolk, livestock raisers, or their authorized representatives. We do not knowingly collect personal data from minors (individuals under 18 years of age) without proper consent from a parent or legal guardian.</p>
                <p>If you are under 18 and wish to register as a beneficiary, please ensure that your parent or legal guardian completes the registration on your behalf and consents to the processing of your data.</p>
                <p>If we become aware that we have collected personal data from a minor without appropriate consent, we will take steps to delete such information promptly.</p>
            </div>

