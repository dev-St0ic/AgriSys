                <h2>2. Description of Service</h2>
                <p>AgriSys is a decision support system developed to modernize and streamline agricultural service delivery for the City Agriculture Office of San Pedro, Laguna. The system provides the following core functionalities:</p>

                <div class="subsection">
                    <h4>2.1 Beneficiary Registration and Management</h4>
                    <ul>
                        <li>Registration of farmers, fisherfolk, livestock raisers, and poultry raisers in the Registry System for Basic Sectors in Agriculture (RSBSA)</li>
                        <li>Maintenance of updated beneficiary profiles including agricultural sector, commodity details, farm information, and contact details</li>
                        <li>Digital archiving of supporting documents and government-issued identification</li>
                    </ul>
                </div>

                <div class="subsection">
                    <h4>2.2 Agricultural Supply Request and Distribution Management</h4>
                    <ul>
                        <li>Online submission of requests for agricultural inputs including seedlings (vegetables, sampaguita, fruit-bearing trees, forest trees), tilapia fingerlings, and organic fertilizers</li>
                        <li>Processing, approval, and tracking of supply requests by City Agriculture Office personnel</li>
                        <li>Real-time monitoring of supply inventory levels with automated alerts when stock falls below critical thresholds</li>
                        <li>Generation of distribution records and acknowledgment receipts</li>
                    </ul>
                </div>

                <div class="subsection">
                    <h4>2.3 Training and Capacity Building</h4>
                    <ul>
                        <li>Application for agricultural training programs, seminars, and workshops</li>
                        <li>Management of community events and agricultural fairs</li>
                        <li>Tracking of participation and completion records</li>
                    </ul>
                </div>

                <div class="subsection">
                    <h4>2.4 Prescriptive Analytics and Decision Support</h4>
                    <ul>
                        <li>Integration of Anthropic's Claude Sonnet 4.5 large language model to analyze agricultural data and generate actionable insights</li>
                        <li>Identification of trends in crop demand, supply distribution patterns, and beneficiary needs</li>
                        <li>Visual dashboards and reports that support evidence-based planning and resource allocation</li>
                        <li>Data-driven recommendations to optimize operational efficiency and service delivery</li>
                    </ul>
                </div>

                <div class="subsection">
                    <h4>2.5 Document Retrieval and Records Management</h4>
                    <ul>
                        <li>Digitization of paper-based records for secure storage and quick retrieval</li>
                        <li>Searchable database of beneficiary information, transaction history, and program documentation</li>
                        <li>Compliance with government archival and record retention standards</li>
                    </ul>
                </div>

